package day1;

public class Operators {

    public static void main(String[] args) {
        int x = 30;
        int y = 40;
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x>=y);
        System.out.println(x<=y);
        System.out.println(x>y || x<y);


    }

}
